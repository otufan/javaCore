package day12_stringManupilation;

public class C05_Contains {

	public static void main(String[] args) {
		
		
		String cumle="Calisirsaniz Java ogrenmek cok kolay";
		
		System.out.println(cumle.contains(" ")); //true
		
		System.out.println(cumle.contains("iz, J")); //false
		
		System.out.println(cumle.contains("va")); //true
		
		
	}

}
